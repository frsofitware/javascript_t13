// --- CONCEITUAÇÃO: PRE-INCREMENTO OU PRE-DECREMENTO ---
// OPERAÇÃO PARA SOMAR (++) OU SUBTRAIR (--) EXATAMENTE 1.

//  DETALHE: A DIFERENÇA ENTRE O PRE E O PÓS É QUANDO ELE 
// APARECE NO CONSOLE. NO CASO DO PRÉ, A EXIBIÇÃO DO VALOR
// É ATUALIZADO DIRETO NA LINHA A QUAL O PRÉ INCREMENTO
// FOI UTILIZADO.

// --- FUNCIONAMENTO DOS CONCEITOS NA PRÁTICA ---

// EXEMPLO DE PRE-INCREMENTO.

let numero = 1

console.log("Valor atual da variável número: ", numero)

console.log("Valor atual da variável número pré incrementada: ", ++ numero)

console.log("------------------------------------------------------------------")


// EXEMPLO DE PRE-DECREMENTO.

console.log("Valor atual da variável número: ", numero)

console.log("Valor atual da variável número pré incrementada: ", -- numero)

console.log("==================================================================")