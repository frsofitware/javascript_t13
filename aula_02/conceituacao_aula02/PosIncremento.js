// --- CONCEITUAÇÃO: POS-INCREMENTO OU POS-DECREMENTO ---
// OPERAÇÃO PARA SOMAR (++) OU SUBTRAIR (--) EXATAMENTE 1.

//  DETALHE: A DIFERENÇA ENTRE O PRE E O PÓS É QUANDO ELE 
// APARECE NO CONSOLE. NO CASO DO PÓS, A EXIBIÇÃO DO VALOR
// É ATUALIZADO SOMENTE AO CHAMAR A VARIÁVEL UMA SEGUNDA
//  VEZ APÓS O USO DO PÓS INCREMENTO NA EQUAÇÃO.

// --- FUNCIONAMENTO DOS CONCEITOS NA PRÁTICA ---

// EXEMPLO DE PRÉ-INCREMENTO.

let numero = 1

console.log("Valor atual da variável número: ", numero)

console.log("Valor atual da variável número pos incrementada: ", numero ++)

console.log("Valor atualizado da variável numero: ", numero)

console.log("------------------------------------------------------------------")


// EXEMPLO DE PRÉ-DECREMENTO.

console.log("Valor atual da variável número: ", numero)

console.log("Valor atual da variável número pos decrementada: ", numero --)

console.log("Valor atualizado da variável numero: ", numero)

console.log("==================================================================")